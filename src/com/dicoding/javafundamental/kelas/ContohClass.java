package com.dicoding.javafundamental.kelas;

public class ContohClass {
    String namaKucing;
    int beratBadan;
    public void KucingAnggora(){
        //class tanpa parameter dan tidak mengembalikan value
    }
    public void KucingAnggora(String nama, int berat){
        //class dengan parameter dan tidak mengembalikan value
        namaKucing = nama;
        beratBadan = berat;
    }
}
